# Multiple Values assigned to Multiple Variables
w, x, y, z = "alpha", "beta", 12, 5.4

# Printing Variables
print("Variable w value is ", w)
print("Variable x value is ", x)
print("Variable y value is ", y)
print("Variable z value is ", z)

print("Variable w is ", type(w))
print("Variable x is ", type(x))
print("Variable y is ", type(y))
print("Variable z is ", type(z))

# Variables
num1=123
str1="alpha"

# list / collection of multiple datatypes enclosed in square brackets[]
# it's mutable you can edit list
first_list = [str1, "Devops", 47, num1, 1.5]

# Printing a list
print(first_list)

# tuple / collection of multiple datatypes enclosed in round brackets ()
# tuples are immutable you can not edit list
first_tuple = (str1, "Devops", 47, num1, 1.5)

# Printing a tuple
print(first_tuple)

# Dictionary / collection of key values pair elements in dictionary are always in key pairs(Key:value) curly brackets {}
first_dictionary = {"Name":"Shrinath", "Weight":73, "Exercises":["Boxing","Dancing","Jogging"]}

# Printing Dictionary
print(first_dictionary)

print("Variable first_list is a:", type(first_list))
print("Variable first_tuple is a:", type(first_tuple))
print("Variable first_dictionary is a:", type(first_dictionary))

# Boolean
